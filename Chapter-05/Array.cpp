#include <iostream>
using namespace std;

int main()
{
    // Initialize an array of 5 integers
    int arr[5] = {10, 20, 30, 40, 50};
    
    // Loop through the array to print each element
    for (int i = 0; i < 5; i++)
    {
        // Output the value of the element at index 'i'
        cout << arr[i] << endl;
    }
    
    return 0;
}